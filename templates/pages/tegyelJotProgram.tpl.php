<h1><b>Tegyél jót program!</b></h1>
<div id="block">
    <img src="./images/tegyeljot_logo.jpg">
    <p>A Tegyél jót! program keretében a Szülők Háza Alapítvány szakemberek bevonásával (szociális munkás, szociálpolitikus, 
        gyermeknevelő, bűnmegelőzési szakember, rekreációs szakember) önkéntes családokat toboroz. Ők a szakemberek által előzetesen 
        feltérképezett gyermekotthonokba látogatnak el és különböző sport, közösségi és élményprogramokat szolgáltatnak a gyerekeknek. 
        Az önkéntesek bevonásánál szempont, hogy életútjuk mintául szolgálhasson és vállalják a rendszerességet. Ennek érdekében a 
        Szülők Háza Alapítvány önkéntes képzéseket, valamint élményfeldolgozó szupervíziót tart.</p>
</div>
<div id="block">
    <div id="left">
    <img src="./images/gyerekek.jpg">
    </div>
    <p>Ma Magyarországon közel 25 ezer gyerek él állami gondoskodásban. Habár ezen intézmények ellátási színvonala nagyon eltérő, 
        összességében kijelenthető, hogy az állami gondoskodásban élő gyerekek életkezdése fokozott nehézséggel bír. Annak ellenére, 
        hogy átfogó statisztika a közelmúltban nem készült arról, hogy milyen a társadalmi integrációjuk és milyen arányban tudnak minőségi életet élni, 
        a szakma és a közvélemény véleménye megegyezik.Eszerint az intézményekből kikerülő gyermekek nagy arányban marginalizálódnak, lépnek a kriminalitás, 
        bűnözés útjára, lesznek szenvedély és pszichiátriai betegek.A mélyszegénységben élő családok jelentős része volt állami gondozott szülőkből áll. 
        Ők a párkapcsolati kultúrát sem tudják hatékonyan elsajátítani.A volt állami gondozottak kevés eszközt, tudást és segítséget kapnak ahhoz, 
        hogy sikeresen boldoguljanak az életben.</p>
</div>

<div id="block">
    <img src="./images/lanyok.jpg">

    <p>A változáshoz hatékony civil és állami összefogás szükséges. Egy olyan mozgalom, amely eljut az állami intézményekbe és személyes 
    kapcsolatok kiépítésével motiválja a gyerekeket a továbbtanulásra, személyes történetekkel példát mutat, lehetőséget biztosít a társadalom aktív tagjaival ápolható kapcsolatokra.</p>
</div>

<div id="block">
    <div id="left">
        <img src="./images/sokan.jpg">
    </div>
    <p>A program célja a gyermekotthonokban eltöltött idő segítése a gyermekek számára. Az élményprogramokkal a családias légkör 
        megteremtése, a motiváció, a kapcsolatteremtés más szociokulturális helyzetben lévő családokkal és gyerekkel. 
        Fontos kitörési a tanulás, egy jó szakma megszerzése, valamint az emberi kapcsolatok megteremetésére való képesség 
        elsajátítása. A Szülők Háza professzionális és önkéntes segítői személyes, rendszeres jelenlétükkel, példamutatásukkal 
        ebben segítik a gyerekeket. Emellett fontos számunkra a nevelőtanárokkal való együttműködés, munkájuk megbecsülése mellett annak segítése, kiegészítése.</p>
</div>

